# Primeira forma de fazer
pt = int(input('Digite o primeiro termo: '))
r = int(input('Digite a razão: '))
c = 0
t = pt
while not c == 10:
	print(t, end=' -> ')
	t += r
	c += 1
print('Fim')

# Segunda forma de fazer

t = int(input('Digite o primeiro termo: '))
r = int(input('Digite a razão: '))
c = 1
t = pt
while  c <= 10:
	print(t, end=' -> ')
	t += r
	c += 1
print('Fim')
	
	